	The Rogue River National Forest (until 1932 called the Crater National Forest) was established by President Theodore Roosevelt in 1908. The name Rogue River commemorates the Takelma Indians, whose defense of their homeland let early day French-Canadian trappers to call them les Coquins, "the Rogues". The Siskiyou Forest Reserve was established by President Theodore Roosevelt in 1905, and the Reserve was designated as the Siskiyou National Forest in 1907. The name Siskiyou is a Cree Indian word for bob-tailed horse (bestowed in 1828 by French Canadians working for the Hudson Bay Company).<br />
	<br />
		Dating back to 1975 the Siskiyou hosted the fourth Region 6 heli-rappelling program, located at the Smokejumper Base in Cave Junction, Oregon. The base was relocated to its present location at the Grants Pass Airport in 1978, and has gone through more than a few facilities changes since then.<br />
<br />
Between the years of 1978 and 1986 the Siskiyou maintained a helicopter on contract with a basic helitack crew. The rappel program reemerged on a limited basis in 1987 when 5 Siskiyou crewmembers went to Chelan, WA to be trained as rappellers once again. Since that time our program has taken on changing roles as a Regional program and local cooperating agencies took new growth. With the changes came more interagency cooperation between the Southwest unit of the Oregon Department of Forestry (ODF), and the combining of the Rogue River NF and the Siskiyou NF..<br />
<br />
From 2001 through 2010 total aircraft flight time has averaged 134.4 flight hours per season. We maintain an average of 49 operational rappels and 40 additional incident responses per season. Our helicopter contract runs from the first week of June to the end of September for a total length of 120 days, and we have the ability to extend as fire season dictates.<br />
<br />
As with all the rappel bases in the region we take great pride in our history and the many accomplishments we have had over the years.<br />
<br />
If you are a former crewmember or a history buff who has any additional information on the program or the forest, please contact the base. We would love to hear about it.
